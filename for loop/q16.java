

public class q16 {
    public static void main(String[]args){
        int n=27;
        boolean isprime=true;
        for(int i=2;i<=n/2;i++){
            if(n%i!=0){
                isprime=false;
                break;
            }
        }
        if(isprime&&n>1)
            System.out.println("Prime");
        else
            System.out.println("Not Prime");


    }
    
}
